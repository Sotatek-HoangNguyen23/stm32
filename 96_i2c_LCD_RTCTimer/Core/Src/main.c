static void lcd_fill_line(char *dst, const char *src)
{
  uint8_t i = 0;

  while (i < 20U && src[i] != '\0')
  {
    dst[i] = src[i];
    i++;
  }

  while (i < 20U)
  {
    dst[i] = ' ';
    i++;
  }

  dst[20] = '\0';
}

static void lcd_print_row(uint8_t row, const char *text)
{
  char line[21];
  lcd_fill_line(line, text);
  lcd_set_cursor(row, 0);
  lcd_send_string(line);
}

static void to_2digits(char *dst, uint8_t value)
{
  dst[0] = (char)('0' + (value / 10U));
  dst[1] = (char)('0' + (value % 10U));
}

static const char *rtc_day_name(uint8_t day)
{
  static const char *names[8] = {"UNK", "S U N D A Y", "M O N D A Y", "T U E S D A Y", "W E D N E S D A Y", "T H U R S D A Y", "F R I D A Y", "S A T U R D A Y"};

  if (day > 7U)
  {
    return names[0];
  }

  return names[day];
}

static void lcd_show_rtc(const DS1307_Time *time)
{
  char line1[21] = "TIME: 00:00:00";
  char line2[21] = "DATE: 00/00/20YY";
  char line3[21] = "DAY: ";
  char line4[21] = "SYNC: BUILD +/-5s";
  const char *day_name = rtc_day_name(time->day);

  to_2digits(&line1[6], time->hours);
  to_2digits(&line1[9], time->minutes);
  to_2digits(&line1[12], time->seconds);

  to_2digits(&line2[6], time->date);
  to_2digits(&line2[9], time->month);
  to_2digits(&line2[14], time->year);

  for (uint8_t i = 0U; i < 20U && day_name[i] != '\0'; ++i)
  {
    line3[5U + i] = day_name[i];
  }

  lcd_print_row(0, line1);
  lcd_print_row(1, line2);
  lcd_print_row(2, line3);
  lcd_print_row(3, line4);
}

